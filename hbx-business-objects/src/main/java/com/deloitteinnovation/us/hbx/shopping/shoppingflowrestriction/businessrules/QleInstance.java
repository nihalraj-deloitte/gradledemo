public class QleInstance {


    private String in_text_qle_name;
    private String the_qle_bucket;
    private String the_qle_shopping_restriction;
    private Integer the_qle_restriction_priority;


    public String getIn_text_qle_name() {
        return in_text_qle_name;
    }

    public void setIn_text_qle_name(String in_text_qle_name) {
        this.in_text_qle_name = in_text_qle_name;
    }

    public String getThe_qle_bucket() {
        return the_qle_bucket;
    }

    public void setThe_qle_bucket(String the_qle_bucket) {
        this.the_qle_bucket = the_qle_bucket;
    }

    public String getThe_qle_shopping_restriction() {
        return the_qle_shopping_restriction;
    }

    public void setThe_qle_shopping_restriction(String the_qle_shopping_restriction) {
        this.the_qle_shopping_restriction = the_qle_shopping_restriction;
    }

    public Integer getThe_qle_restriction_priority() {
        return the_qle_restriction_priority;
    }

    public void setThe_qle_restriction_priority(Integer the_qle_restriction_priority) {
        this.the_qle_restriction_priority = the_qle_restriction_priority;
    }

}
